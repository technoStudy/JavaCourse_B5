package Day2;

public class _04_EscapeSequences {

    public static void main(String[] args) {

        // To use double quotes we need back slash
        System.out.println("I learn \"Java\" in TechnoStudy");

        // To use back slash you need back slash
        System.out.println("C:\\ProgramFiles\\...");

        // To move a new line - \n
        System.out.println("Java is a very good\nprogramming language!");

        // To use tab - \t
        System.out.println("TechnoStudy");
        System.out.println("\tTechnoStudy");

    }

}
