package Day16;

public class _01_InfiniteLoop {

    public static void main(String[] args) {

        // Infinite Loop (with For)
        for (int i = 0; i < 1;) {
            System.out.println("Hello World!");
        }


        // Infinite Loop (with While)
        while (true) {
            System.out.println("TechnoStudy");
        }

    }

}
