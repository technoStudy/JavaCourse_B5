package Day9;

public class Task {

    /*
                TASK
        double num1 = 23.4;
        double num2 = 24.0;
        double num3 = 12;
        double num4 = 450.3;
        double num5 = 23000;

        find the average for this doubles

     */


    public static void main(String[] args) {

        double num1 = 23.4;
        double num2 = 24.0;
        double num3 = 12;
        double num4 = 450.3;
        double num5 = 23000;

        double sum = num1 + num2 + num3 + num4 + num5;

        double average = sum / 5;

        System.out.println("Average -> " + average);

    }

}
