package Day8.ArithmeticExpressions;

public class _05_Modulus {

    public static void main(String[] args) {

        int x = 8; // dividend
        int y = 3; // divisor

        System.out.println(x % y); // remainder from division

        System.out.println(10 % 3);

        System.out.println(100 % 10);
    }

}
