package Day17;

public class _01_NestedLoops {

    public static void main(String[] args) {

        // int i = 0
        for (int i = 0; i < 5; i++) {
            System.out.println("i = " + i);

            // j = 0
            for (int j = 0; j < 5; j++) {
                System.out.println("     j = " + j);
            }

        }
    }

}
