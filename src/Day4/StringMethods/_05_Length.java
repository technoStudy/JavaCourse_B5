package Day4.StringMethods;

public class _05_Length {

    public static void main(String[] args) {

        String longString = "asdfghjkl1234567890qwertyuiopzxc2356543";

        int lengthOfLongString = longString.length();

        System.out.println("Length of String: " + lengthOfLongString);

        String shortString = "abc";

        System.out.println(shortString.length());

    }

}
