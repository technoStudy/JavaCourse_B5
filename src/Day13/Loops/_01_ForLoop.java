package Day13.Loops;

public class _01_ForLoop {

    public static void main(String[] args) {

//        System.out.println("Hello World!");
//        System.out.println("Hello World!");
//        System.out.println("Hello World!");
//        System.out.println("Hello World!");
//        System.out.println("Hello World!");


        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World! " + i);
        }


        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello World! " + i);
        }


        for (int i = 5; i > 0; i--) {
            System.out.println("Hello World! " + i);
        }



    }

}
